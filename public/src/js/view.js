function showHome() {
  $('#menuPrincipal').show();
  $('#menu').show();
  $('#saldo').hide();
  $('#calcular').hide();
  $('#perfil').hide();
  $('#preguntas').hide();
}

function showSaldo() {
  $('#menuPrincipal').show();
  $('#menu').hide();
  $('#saldo').show();
  $('#calcular').hide();
  $('#perfil').hide();
  $('#preguntas').hide();
}

function showCalcular() {
  $('#menuPrincipal').show();
  $('#menu').hide();
  $('#saldo').hide();
  $('#calcular').show();
  $('#perfil').hide();
  $('#preguntas').hide();
}

function showPerfil() {
  $('#menuPrincipal').show();
  $('#menu').hide();
  $('#saldo').hide();
  $('#calcular').hide();
  $('#perfil').show();
  $('#preguntas').hide();
}

function showPreguntas() {
  $('#menuPrincipal').show();
  $('#menu').hide();
  $('#saldo').hide();
  $('#calcular').hide();
  $('#perfil').hide();
  $('#preguntas').show();
}


